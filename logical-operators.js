//Logical Operators
//  && ||

// && will be true when both operations are true.
console.log(10 == 10 && "A" == "A"); //true
console.log(10 == 10 && "A" == "B"); //false

// || will bee true  as  long  as  one of the operations is  true.
console.log(10 == 10 || "A" == "A"); //true
console.log(10 == 10 || "A" == "B"); //true
